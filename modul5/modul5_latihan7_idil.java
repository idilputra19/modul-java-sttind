public class modul5_latihan7_idil{
public static void main (String[] args) {
int harike = 1;
switch (harike) {
case 1 :
 System.out.println(" Harike - " + harike + " adalah hari Senin");
 break;
case 2 :
 System.out.println(" Harike - " + harike + " adalah hari Selasa");
 break;
case 3 :
 System.out.println(" Harike - " + harike + " adalah hari Rabu");
 break;
case 4 :
 System.out.println(" Harike - " + harike + " adalah hari Kamis");
 break;
case 5 :
 System.out.println(" Harike - " + harike + " adalah hari Jumat");
 break;
case 6 :
 System.out.println(" Harike - " + harike + " adalah hari Sabtu");
 break;
default:
 System.out.println(" Harike - " + harike + " adalah hari Minggu");
}
}
}
