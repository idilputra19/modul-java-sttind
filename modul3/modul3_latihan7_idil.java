public class modul3_latihan7_idil{
public static void main (String[] args) {
System.out.println("Tabel Kebenaran operator logika and");
System.out.println("Benar AND benar = "+ (true && true));
System.out.println("Benar AND Salah = "+ (true && false));
System.out.println("Salah AND benar = "+ (false && true));
System.out.println("Salah AND Salah = "+ (false && false));
System.out.println("");
System.out.println("Tabel Kebenaran operator logika OR");
System.out.println("Benar OR benar = "+ (true || true));
System.out.println("Benar OR Salah = "+ (true || false));
System.out.println("Salah OR benar = "+ (false || true));
System.out.println("Salah OR Salah = "+ (false || false));
System.out.println("");
System.out.println("Tabel Kebenaran operator logika XOR");
System.out.println("Benar XOR benar = "+ (true ^ true));
System.out.println("Benar XOR Salah = "+ (true ^ false));
System.out.println("Salah XOR benar = "+ (false ^ true));
System.out.println("Salah XOR Salah = "+ (false ^ false));
System.out.println("");
System.out.println("Tabel Kebenaran operator logika NOT");
System.out.println("NOT benar = "+ (! true));
System.out.println("NOT Salah = "+ (! false));
}
}
