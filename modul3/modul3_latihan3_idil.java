public class modul3_latihan3_idil{
public static void main (String[] args) {
int a=23;
System.out.println("increment dan decrement :");
System.out.println("Proses pre-increment dan post-increment :");
++a;
System.out.println("a\t pre-increment\t= "+ a);
a++;
System.out.println("a\t post-increment\t= "+ a);
System.out.println("");
System.out.println("Proses pre-decrement dan post-decrement");
--a;
System.out.println("a\t pre-increment\t= "+ a);
a--;
System.out.println("a\t post-increment\t= "+ a);
System.out.println("");
System.out.println("Nilai a sekarang adalah "+ a);
}
} 