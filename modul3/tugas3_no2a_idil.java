public class tugas3_no2a_idil {
    public static void main (String [] args) {
        long p = 517;  // Panjang
        long l = 317;  // Lebar
        long t = 717;  // Tinggi
        long v;

        v = p * l * t;
        
        // Menampilkan Panjang x Lebar x Tinggi
        System.out.println("Dimensi Balok: " + p + " x " + l + " x " + t);
        System.out.println("Hasil dari Volume Balok: " + v);
    }
}