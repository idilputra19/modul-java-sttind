public class tugas2no3_idil {
    public static void main(String[] args) {
        int x = 17; // X sesuai NPM Yaitu 17
        int y;

        // Menghitung nilai y berdasarkan rumus
        y = (7 * x * x * x) + (5 * x) + (4 * x * x * x * x) - 5;

        // Menampilkan hasil perhitungan
        System.out.println("Hasil dari f(x) untuk x = " + x + " adalah: " + y);
    }
}
